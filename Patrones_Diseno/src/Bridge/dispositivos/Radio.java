package Bridge.dispositivos;

import Bridge.Dispositivo;

/**
 *
 * @author DELL
 */
public class Radio implements Dispositivo{
    
    private boolean on = false;
    private int volume = 30;
    private int channel = 1;

    @Override
    public boolean isEnabled() {
        return on;
    }

    @Override
    public void enable() {
        on = true;
    }

    @Override
    public void disable() {
        on = false;
    }

    @Override
    public int getVolume() {
        return volume;
    }

    @Override
    public void setVolume(int percent) {
        if (volume > 100) {
            this.volume = 100;
        } else if (volume < 0) {
            this.volume = 0;
        } else {
            this.volume = volume;
        }
    }

    @Override
    public int getChannel() {
        return channel;
    }

    @Override
    public void setChannel(int channel) {
        this.channel = channel;
    }

    @Override
    public void printStatus() {
        System.out.println("------------------------------------");
        System.out.println("| Soy un radio");
        System.out.println("| Estoy " + (on ? "habilitado" : "deshabilitado"));
        System.out.println("| El volumen actual es del " + volume + "%");
        System.out.println("| La emisora actual es la #" + channel);
        System.out.println("------------------------------------\n");
    }
    
}
